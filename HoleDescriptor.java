public class HoleDescriptor{
  private int first;
  private int last;

  public HoleDescriptor(int f, int l){
    this.first = f;
    this.last = l;
  }

	public int getFirst() {
		return first;
	}

	public void setFirst(int first) {
		this.first = first;
	}

	public int getLast() {
		return last;
	}

	public void setLast(int last) {
		this.last = last;
	}
}
